# Concept
# -------
# This script performs bias correction and statistical downscaling
# of subseasonal–seasonal (S2S) or seasonal forecast data within the
# AgWise / AgWISE_Forecast workflow. It uses:
#
#   - Historical hindcast data to learn systematic model biases
#     relative to high-quality reference observations, and
#   - Applies the derived correction to real-time or target-year
#     forecasts to produce bias-adjusted, analysis-ready fields.
#
# The workflow is designed around:
#
#   * Scientific transparency:
#       - Explicit use of hindcasts for calibration only.
#       - Clearly defined reference period and spatial domain.
#   * Consistency across variables and models:
#       - Common folder structure shared with the Python
#         AgWise_Download module.
#       - Unified handling of multiple variables
#         (e.g. PRCP, TMAX, TMIN, TEMP, SRAD).
#   * Operational readiness:
#       - Inputs are taken from standardized "raw" directories
#         (obs + model files generated by Python).
#       - Outputs are written to "processed" directories, separated
#         by country, variable, and forecast year.
#
# Core logic
# ----------
# 1. Read country-specific configuration from 00_config_<COUNTRY>.R,
#    including:
#       - Base directories and country code
#       - Raw and processed folder paths
#       - Calibration years (hindcast period)
#       - Spatial domain (lon/lat limits)
#
# 2. For each variable of interest (e.g. PRCP, TMAX, TMIN, TEMP, SRAD):
#       - Load observation time series for the calibration period.
#       - Load hindcast model data for the same period.
#       - Load corresponding real-time forecasts to be corrected.
#
# 3. Perform bias correction / statistical downscaling:
#       - Estimate biases using hindcasts vs observations.
#       - Apply quantile delta mapping (QDM) or similar method
#         to the forecast data, treating hindcasts only as the
#         calibration set.
#
# 4. Export bias-corrected forecasts:
#       - Save corrected forecast fields as NetCDF in a consistent
#         naming convention under:
#           processed/bias_corrected/forecast/
#       - Optionally generate spatial bias maps for diagnostic
#         purposes (hindcast vs obs, and bias-corrected forecast).
#
# Intended use
# ------------
# This module serves as the R-based post-processing layer for
# AgWise / AgWISE_Forecast and related national agro-climate
# advisory systems. It bridges raw model output and downstream
# applications such as:
#
#   - Agro-climate indices and extremes,
#   - Crop and livestock models (e.g. DSSAT),
#   - Advisory products and decision-support tools,
#   - Skill assessment and verification workflows.
#
