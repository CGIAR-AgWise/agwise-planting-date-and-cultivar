run_agwise_seasonal_forecast_BC <- function(
    country_code = "KEN",
    init_month_user = 10, 
    init_day_user = 1,
    season_length_months = 3,
    forecast_year = as.integer(format(Sys.Date(), "%Y")),
    forecast_lead_months = 1,
    use_manual_extent = FALSE, extent_manual = c(16, 34, 8, 40),
    manual_domain_name = "User_Domain",
    base_dir=normalizePath("../../data", mustWork = FALSE),
    main_script_dir = normalizePath(".", mustWork = TRUE),
    py_path = "/opt/anaconda3/envs/WASS2S/bin/python",
    py_script = file.path(main_script_dir, "02_run_agwise_multi_country.py"),
    dssat_geo_script = file.path(main_script_dir, "04_prepare_dssat_geo_inputs.R"),
    variables_to_bc = c("PRCP", "TMAX", "TMIN", "TEMP", "SRAD"),
    force_download = FALSE,
    export_dssat = TRUE,
    n_cores = NULL,
    run_downloader = TRUE,
    parallel_backend = c("auto", "process", "fork", "none")
) {
  
    setwd(main_script_dir)
    parallel_backend <- match.arg(parallel_backend)
      
    #############################################################
    # 1 Build country configration files for both forecast and bias correction
    ###############################################################
    
    build_country_config(country_code = country_code, base_dir = base_dir, 
                         use_manual_extent = use_manual_extent, extent_manual = extent_manual,
                         manual_domain_name = manual_domain_name,
                         init_month_user = init_month_user, init_day_user = init_day_user,
                         season_length_months = season_length_months,
                         forecast_year = forecast_year,
                         forecast_lead_months = forecast_lead_months)
    
    
    config_json_path <- file.path(
      country_data_dir(base_dir, country_code), "config",
      paste0(country_code, "_config_agwise.json"))
    aoi_config <- load_country_config_from_json(config_json_path)
    season_length <- aoi_config$season_length_months
    if (is.null(season_length) || is.na(season_length)) season_length <- 3
    seasons <- ((aoi_config$init_month - 1 + 0:(season_length - 1)) %% 12) + 1
    forecast_dates_from_file <- function(path) {
      nc <- ncdf4::nc_open(path)
      on.exit(ncdf4::nc_close(nc), add = TRUE)
      time_dim <- nc$dim[["time"]]
      if (is.null(time_dim)) time_dim <- nc$dim[["T"]]
      if (is.null(time_dim)) {
        stop("Forecast file has no time dimension: ", path)
      }
      units <- time_dim$units
      vals <- time_dim$vals
      if (is.null(units) || length(vals) < 1) {
        stop("Forecast file has no valid time coordinate: ", path)
      }
      origin_text <- sub("^[A-Za-z]+ since[[:space:]]+", "", units)
      origin_text <- sub("[[:space:]]+UTC$", "", origin_text)
      origin <- as.POSIXct(origin_text, tz = "UTC")
      if (is.na(origin)) {
        origin <- as.POSIXct(as.Date(origin_text), tz = "UTC")
      }
      unit_name <- tolower(strsplit(units, "[[:space:]]+")[[1]][1])
      multiplier <- switch(unit_name,
        "days" = 86400, "day" = 86400,
        "hours" = 3600, "hour" = 3600,
        "minutes" = 60, "minute" = 60,
        "seconds" = 1, "second" = 1,
        stop("Unsupported time units in forecast file: ", units)
      )
      as.Date(origin + vals * multiplier, tz = "UTC")
    }
    forecast_months_from_file <- function(path) {
      dates <- forecast_dates_from_file(path)
      dates <- dates[!is.na(dates)]
      if (length(dates) < 1) {
        stop("Forecast file has no valid daily time coordinate: ", path)
      }
      unique(as.integer(format(dates, "%m")))
    }
    format_months <- function(months) {
      paste(sprintf("%02d", months), collapse = ",")
    }
    resolve_n_cores <- function(requested = NULL) {
      requested <- suppressWarnings(as.integer(requested))
      detected <- suppressWarnings(parallel::detectCores())
      if (!is.finite(detected) || is.na(detected)) detected <- NA_integer_

      if (!length(requested) || !is.finite(requested) || is.na(requested) || requested < 1L) {
        requested <- if (is.na(detected)) 1L else max(1L, detected - 1L)
      }
      if (!is.na(detected)) requested <- min(requested, detected)
      max(1L, requested)
    }
    can_use_downscaler_parallel <- function(cores) {
      if (!isTRUE(cores > 1L)) return(FALSE)
      cl <- try(parallel::makeCluster(cores, type = "FORK"), silent = TRUE)
      if (inherits(cl, "try-error")) return(FALSE)
      parallel::stopCluster(cl)
      TRUE
    }
    r_quote <- function(x) {
      paste0('"', gsub('(["\\\\])', '\\\\\\1', x), '"')
    }
    r_bool <- function(x) {
      if (isTRUE(x)) "TRUE" else "FALSE"
    }
    r_num_vec <- function(x) {
      paste0("c(", paste(as.numeric(x), collapse = ","), ")")
    }
    r_char_vec <- function(x) {
      paste0("c(", paste(vapply(x, r_quote, character(1)), collapse = ","), ")")
    }
    configured_init_token <- function(year = NULL) {
      token <- paste0(month.abb[as.integer(aoi_config$init_month)], as.integer(aoi_config$init_day))
      if (!is.null(year)) token <- paste0(token, "_", as.integer(year))
      paste0("_", token, "_")
    }
    n_cores <- resolve_n_cores(n_cores)
    message("Bias-correction core budget: ", n_cores)

    
    t_all <- Sys.time()
    
    t_down <- Sys.time()
    #############################################################
    # 2 Run Python multi-country downloader & lead time forecast BEFORE bias correction
    ###############################################################
    if (isTRUE(run_downloader)) {
      message("Running AgWise Python downloader for country: ", country_code)

      py_cmd    <- sprintf("%s %s --data-dir %s --countries %s", shQuote(py_path), shQuote(py_script),shQuote(base_dir), shQuote(country_code))
      if (isTRUE(force_download)) py_cmd <- paste(py_cmd, "--force-download")

      # Execute command
      status <- system(py_cmd, intern = TRUE)
      py_status <- attr(status, "status")

      message("Python downloader output:")
      print(status)
      if (!is.null(py_status) && py_status != 0) {
        stop("Python downloader failed with exit status ", py_status)
      }
    } else {
      message("Skipping Python downloader in bias-correction worker.")
    }
    message("Downloader finished Time: ", round(difftime(Sys.time(), t_down, units = "mins"), 2), " min")
    
    message("Proceeding to bias correction.........")

    run_dssat_export <- function() {
      if (!isTRUE(export_dssat)) return(invisible(NULL))
      message("Preparing DSSAT geo RDS forecast inputs for readGeo_CM_zone.R...")
      t_dssat <- Sys.time()
      tmp_dir <- Sys.getenv(
        "TMPDIR",
        unset = "/Volumes/T7/tmp"
      )
      dir.create(tmp_dir, recursive = TRUE, showWarnings = FALSE)
      dssat_status <- system2(
        "Rscript",
        args = c(dssat_geo_script, "--config", config_json_path, "--overwrite"),
        stdout = TRUE,
        stderr = TRUE,
        env = paste0("TMPDIR=", tmp_dir)
      )
      dssat_exit <- attr(dssat_status, "status")
      message("DSSAT geo input preparation output:")
      print(dssat_status)
      if (!is.null(dssat_exit) && dssat_exit != 0) {
        stop("DSSAT geo input preparation failed with exit status ", dssat_exit)
      }
      message("DSSAT geo input preparation time: ", round(difftime(Sys.time(), t_dssat, units = "mins"), 2), " min")
      invisible(dssat_status)
    }
    
    #############################################################
    ###3 Bias Correction based on Observation, hindicast and forecast data
    #############################################################
    
    bc_var_cfg <- list(
      PRCP = list(obs_file_pattern="Daily_PRCP_%d_%d.nc", obs_var="precip",
                  model_var="PRCP", is_precip=TRUE, method="ptr",
                  scaling.type="multiplicative", units="mm/day"),
      TMAX = list(obs_file_pattern="Daily_TMAX_%d_%d.nc", obs_var="TMAX",
                  model_var="TMAX", is_precip=FALSE, method="qdm",
                  scaling.type="additive", units="degC"),
      TMIN = list(obs_file_pattern="Daily_TMIN_%d_%d.nc", obs_var="TMIN",
                  model_var="TMIN", is_precip=FALSE, method="qdm",
                  scaling.type="additive", units="degC"),
      TEMP = list(obs_file_pattern="Daily_TEMP_%d_%d.nc", obs_var="TEMP",
                  model_var="TEMP", is_precip=FALSE, method="qdm",
                  scaling.type="additive", units="degC"),
      SRAD = list(obs_file_pattern="Daily_SRAD_%d_%d.nc", obs_var="SRAD",
                  model_var="SRAD", is_precip=FALSE, method="qdm",
                  scaling.type="multiplicative", units="MJ/m2/day"))
    
    ## Variables to process
    variables_to_bc <- intersect(variables_to_bc, names(bc_var_cfg))
    
    if (length(variables_to_bc) == 0) {
      stop("No valid variables selected for bias correction.")
    }

    if (parallel_backend == "auto") {
      parallel_backend <- if (isTRUE(run_downloader) && n_cores > 1L && length(variables_to_bc) > 1L) "process" else "none"
    }

    run_variable_worker <- function(var_code) {
      log_dir <- file.path(country_data_dir(base_dir, country_code), "forecast", "logs")
      dir.create(log_dir, recursive = TRUE, showWarnings = FALSE)
      log_file <- file.path(
        log_dir,
        paste0("bias_correction_", var_code, "_", format(Sys.time(), "%Y%m%d%H%M%S"), "_pid", Sys.getpid(), ".log")
      )
      expr <- paste0(
        "source(", r_quote(file.path(main_script_dir, "03_bias_correction_forecast_multiVar.R")), "); ",
        "run_agwise_seasonal_forecast_BC(",
        "country_code=", r_quote(country_code),
        ", init_month_user=", as.integer(init_month_user),
        ", init_day_user=", as.integer(init_day_user),
        ", season_length_months=", as.integer(season_length_months),
        ", forecast_year=", as.integer(forecast_year),
        ", forecast_lead_months=", as.integer(forecast_lead_months),
        ", use_manual_extent=", r_bool(use_manual_extent),
        ", extent_manual=", r_num_vec(extent_manual),
        ", manual_domain_name=", r_quote(manual_domain_name),
        ", base_dir=", r_quote(normalizePath(base_dir, mustWork = FALSE)),
        ", main_script_dir=", r_quote(normalizePath(main_script_dir, mustWork = TRUE)),
        ", py_path=", r_quote(py_path),
        ", py_script=", r_quote(py_script),
        ", dssat_geo_script=", r_quote(dssat_geo_script),
        ", variables_to_bc=", r_char_vec(var_code),
        ", force_download=FALSE",
        ", export_dssat=FALSE",
        ", n_cores=1",
        ", run_downloader=FALSE",
        ", parallel_backend=\"none\"",
        ")"
      )
