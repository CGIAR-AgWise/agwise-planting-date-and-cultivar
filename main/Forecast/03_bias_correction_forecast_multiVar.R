        details <- vapply(worker_results[failed], function(x) {
          paste0(x$var_code, " failed with status ", x$status, ". Log: ", x$log_file)
        }, character(1))
        stop("Parallel bias-correction worker failure(s):\n", paste(details, collapse = "\n"))
      }
      message("Process-level bias correction completed. Worker logs:")
      print(vapply(worker_results, `[[`, character(1), "log_file"))
      run_dssat_export()
      message("Forecast is ready for all variables: Total runtime: ", round(difftime(Sys.time(), t_all, units = "mins"), 2), " min")
      return(invisible(worker_results))
    }
    
    process_bc_job <- function(job) {
      var_code <- job$var_code
      cfg <- job$cfg
      m <- job$model_id
      obs_file <- job$obs_file
      hind_file <- job$hind_file
      fcst_file <- job$fcst_file
      years_calib <- job$years_calib
      forecast_months <- job$forecast_months
      inner_cores <- job$inner_cores
      use_inner_parallel <- isTRUE(job$use_inner_parallel && inner_cores > 1L)

      if (job$outer_parallel) {
        Sys.setenv(
          OMP_NUM_THREADS = "1",
          OPENBLAS_NUM_THREADS = "1",
          MKL_NUM_THREADS = "1",
          VECLIB_MAXIMUM_THREADS = "1",
          NUMEXPR_NUM_THREADS = "1"
        )
      }

      message("---------------------------------------------------")
      message("Processing ", var_code, " for model ID: ", m)
      message("  Hindcast file: ", hind_file)
      message("  Forecast file: ", fcst_file)
      message("  Forecast lead months used for BC: ", format_months(forecast_months))
      message("  Worker PID: ", Sys.getpid(), " | downscaleR internal cores: ",
              ifelse(use_inner_parallel, inner_cores, 1L))

      ###########################################################
      # 4.3 Load observed calibration data (reference)
      ###########################################################
      nc_obs <- loadGridData(dataset = obs_file, season = forecast_months, var = cfg$obs_var,
        lonLim  = c(aoi_config$extent_obs[2], aoi_config$extent_obs[4]),
        latLim  = c(aoi_config$extent_obs[3], aoi_config$extent_obs[1]),
        years   = years_calib )
      attr(nc_obs$Variable, "units") <- cfg$units

      #--------------------------------------------------------
      # 4.4.1 Load hindcast (calibration) data
      #--------------------------------------------------------
      nc_hind <- tryCatch({
        loadGridData(
          dataset = hind_file,
          var     = cfg$model_var,
          season = forecast_months,
          lonLim  = c(aoi_config$extent_model[2], aoi_config$extent_model[4]),
          latLim  = c(aoi_config$extent_model[3], aoi_config$extent_model[1]),
          years   = years_calib
        )
      }, error = function(e) {
        message("  Failed to load hindcast: ", hind_file)
        message("  Error: ", e$message)
        return(NULL)
      })

      if (is.null(nc_hind)) {
        message("  -> Hindcast load returned NULL, skipping model ", m)
        return(list(status = "skipped", var_code = var_code, model_id = m,
                    reason = "hindcast load returned NULL"))
      }

      #--------------------------------------------------------
      # 4.4.2 Load forecast data (to be bias-corrected)
      #--------------------------------------------------------
      nc_fcst <- tryCatch({
        loadGridData(
          dataset = fcst_file,
          var     = cfg$model_var,
          season = forecast_months,
          lonLim  = c(aoi_config$extent_model[2], aoi_config$extent_model[4]),
          latLim  = c(aoi_config$extent_model[3], aoi_config$extent_model[1])
          ## Keep all available forecast years/lead times; do not pass
          ## years= here because operational forecasts may span years.
        )
      }, error = function(e) {
        message("  Failed to load forecast: ", fcst_file)
        message("  Error: ", e$message)
        return(NULL)
      })

      if (is.null(nc_fcst)) {
        message("  -> Forecast load returned NULL, skipping model ", m)
        return(list(status = "skipped", var_code = var_code, model_id = m,
                    reason = "forecast load returned NULL"))
      }

      #--------------------------------------------------------
      # 4.4.3 Bias correction: hindcast calibrates, forecast is newdata
      #--------------------------------------------------------
      t_bc <- Sys.time()

      message("  -> Running biasCorrection (using hindcast as training data, forecast as newdata)...")
      bc_method <- cfg$method
      if (is.null(bc_method) || is.na(bc_method)) {
        bc_method <- if (cfg$is_precip) "ptr" else "qdm"
      }
      if (!cfg$is_precip && tolower(bc_method) == "ptr") {
        warning("PTR is only valid for precipitation; using QDM for ", var_code, ".")
        bc_method <- "qdm"
      }
      nc_fcst_bc <- biasCorrection(y = nc_obs, x = nc_hind, newdata = nc_fcst,
        precipitation = cfg$is_precip, window = c(30, 7), method = bc_method,
        #cross.val     = "kfold",
        #folds = list(1990:1996, 1997:2002, 2011:2016),
        #consecutive   = cfg$is_precip,
        scaling.type  = cfg$scaling.type, parallel = use_inner_parallel,
        max.ncores = inner_cores, ncores = inner_cores )

      ## Bias-corrected forecast regridded to obs grid
      nc_fcst_bc_interp <- interpGrid(nc_fcst_bc, getGrid(nc_obs), ncores = inner_cores)

      message("BC time: ", round(difftime(Sys.time(), t_bc, units = "mins"), 2), " min")

      #--------------------------------------------------------
      # 4.4.4 Export bias-corrected FORECAST only
      #--------------------------------------------------------
      fcst_years_str <- paste(aoi_config$forecast_year, collapse = "-")
      outFile <- file.path(aoi_config$dir_bc_fcst,
        sprintf("%s_daily_%s_forecast_BC_%s.nc", var_code, m, fcst_years_str))

      message("  -> Writing bias-corrected FORECAST NetCDF: ", outFile)
      t_nc <- Sys.time()

      nc_fcst_bc_interp$Variable$varName = cfg$model_var
      attr(nc_fcst_bc_interp$Variable, "longname") <- "Bias-corrected seasonal forecast"

      nc_bc_rast <- terra::rast(grid2sp(nc_fcst_bc_interp))
      time(nc_bc_rast) <- as.Date(nc_fcst_bc_interp$Dates$start)

      aoi <- terra::vect(file.path(aoi_config$dir_raw_admin, "gadm",
                                   paste0("gadm41_", country_code, "_0_pk.rds")))
      nc_bc_rast_masked <- terra::mask(nc_bc_rast, aoi)

      terra::writeCDF(x = nc_bc_rast_masked, filename = outFile,
                      varname = as.character(nc_fcst_bc_interp$Variable$varName),
                      unit    = as.character(attr(nc_fcst_bc_interp$Variable, "units")),
                      atts     = globalAttributeList,
                      prec     = "double", missval  = -999, compression = 9, overwrite = TRUE)

      message("NetCDF write time: ", round(difftime(Sys.time(), t_nc, units = "secs"), 1), " s")
      message("  -> Finished ", var_code, " for model ID: ", m)

      list(status = "ok", var_code = var_code, model_id = m, outFile = outFile)
    }

    run_bc_job_safely <- function(job) {
      tryCatch(
        process_bc_job(job),
        error = function(e) {
          list(status = "error", var_code = job$var_code,
               model_id = job$model_id, reason = conditionMessage(e))
        }
      )
    }

    ###############################################################
    # 4. Build independent variable/model jobs, then run in parallel
    ###############################################################
    years_calib <- c(aoi_config$year_hndS:aoi_config$year_hndE)
    jobs <- list()
    for (var_code in variables_to_bc) {
      cfg <- bc_var_cfg[[var_code]]
      message("===================================================")
      message("Variable: ", var_code)
      message(" -> obs_file_pattern: ", cfg$obs_file_pattern)
      message(" -> obs_var: ", cfg$obs_var, " | model_var: ", cfg$model_var)

      obs_file <- file.path(aoi_config$dir_raw_obs, 
                          sprintf(cfg$obs_file_pattern, aoi_config$year_start_obs, aoi_config$year_end_obs))

      ###########################################################
      # 4.2 Hindcast & forecast files for this variable
      #     Python naming:
      #       hindcast_<modelid>_<VAR>_...
      #       forecast_<modelid>_<VAR>_...
      ###########################################################
      hind_pattern <- sprintf("^hindcast_.*_%s_.*\\.nc$", var_code)
      fcst_pattern <- sprintf("^forecast_.*_%s_.*\\.nc$", var_code)
      
      hind_files <- list.files(aoi_config$dir_raw_model, pattern = hind_pattern,  full.names = TRUE)
      fcst_files <- list.files(aoi_config$dir_raw_model, pattern = fcst_pattern,full.names = TRUE)
      hind_files <- hind_files[grepl(configured_init_token(), basename(hind_files), fixed = TRUE)]
      fcst_files <- fcst_files[grepl(
        configured_init_token(aoi_config$forecast_init_year),
        basename(fcst_files),
        fixed = TRUE
      )]
      if (length(hind_files) == 0) {
        message("  !! No hindcast files found for configured initialization token: ", configured_init_token())
      }
      if (length(fcst_files) == 0) {
        message(
          "  !! No forecast files found for configured initialization token: ",
          configured_init_token(aoi_config$forecast_init_year)
        )
      }
      
      ## Extract model IDs (e.g. "ecmwf51") so we can pair hindcast + forecast
      hind_ids <- sub("^hindcast_([^_]+)_.+\\.nc$", "\\1", basename(hind_files))
      fcst_ids <- sub("^forecast_([^_]+)_.+\\.nc$", "\\1", basename(fcst_files))
      
      model_ids <- intersect(unique(hind_ids), unique(fcst_ids))
      if (length(model_ids) == 0) {
        message("  !! No common model IDs between hindcast and forecast for ", var_code)
        next
      }

      for (m in model_ids) {
        hind_file <- hind_files[hind_ids == m][1]
        fcst_file <- fcst_files[fcst_ids == m][1]
        forecast_months <- forecast_months_from_file(fcst_file)
        jobs[[length(jobs) + 1L]] <- list(
          var_code = var_code, cfg = cfg, model_id = m,
          obs_file = obs_file, hind_file = hind_file, fcst_file = fcst_file,
          years_calib = years_calib, forecast_months = forecast_months
        )
      }
    }

    if (length(jobs) < 1L) {
      stop("No bias-correction jobs were created. Check raw model files and selected variables.")
    }
    outer_cores <- if (parallel_backend == "fork" && .Platform$OS.type != "windows") min(n_cores, length(jobs)) else 1L
    inner_cores <- if (outer_cores > 1L) 1L else n_cores
    use_inner_parallel <- outer_cores == 1L && can_use_downscaler_parallel(inner_cores)
    if (outer_cores == 1L && inner_cores > 1L && !use_inner_parallel) {
      message("downscaleR internal parallel backend is unavailable; running this job sequentially.")
    }
    jobs <- lapply(jobs, function(job) {
      job$outer_parallel <- outer_cores > 1L
      job$inner_cores <- inner_cores
      job$use_inner_parallel <- use_inner_parallel
      job
    })

    message(
      "Bias-correction jobs: ", length(jobs),
      " | outer workers: ", outer_cores,
      " | downscaleR internal cores per job: ", ifelse(use_inner_parallel, inner_cores, 1L)
    )

    bc_results <- if (outer_cores > 1L) {
      parallel::mclapply(jobs, run_bc_job_safely, mc.cores = outer_cores, mc.preschedule = FALSE)
    } else {
      lapply(jobs, run_bc_job_safely)
    }

    failed <- vapply(bc_results, function(x) identical(x$status, "error"), logical(1))
    if (any(failed)) {
      details <- vapply(bc_results[failed], function(x) {
        paste0(x$var_code, "/", x$model_id, ": ", x$reason)
      }, character(1))
      stop("Bias correction failed for job(s):\n", paste(details, collapse = "\n"))
    }
    skipped <- vapply(bc_results, function(x) identical(x$status, "skipped"), logical(1))
    if (any(skipped)) {
      details <- vapply(bc_results[skipped], function(x) {
        paste0(x$var_code, "/", x$model_id, ": ", x$reason)
      }, character(1))
      message("Skipped bias-correction job(s):\n", paste(details, collapse = "\n"))
    }
    message("Completed bias-correction jobs: ", sum(vapply(bc_results, function(x) identical(x$status, "ok"), logical(1))))
    
    run_dssat_export()
    
    message("Forecast is ready for all variables: Total runtime: ", round(difftime(Sys.time(), t_all, units = "mins"), 2), " min")
}
