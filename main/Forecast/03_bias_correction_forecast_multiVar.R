        paste0("TMPDIR=", Sys.getenv("TMPDIR", unset = "/Volumes/T7/tmp")),
        "OMP_NUM_THREADS=1",
        "OPENBLAS_NUM_THREADS=1",
        "MKL_NUM_THREADS=1",
        "VECLIB_MAXIMUM_THREADS=1",
        "NUMEXPR_NUM_THREADS=1"
      )
      status <- system2("Rscript", c("-e", shQuote(expr)), stdout = log_file, stderr = log_file, env = env)
      list(status = status, var_code = var_code, log_file = log_file)
    }

    if (parallel_backend == "process" && isTRUE(run_downloader) && n_cores > 1L && length(variables_to_bc) > 1L) {
      worker_cores <- min(n_cores, length(variables_to_bc))
      message(
        "Launching process-level bias correction: ", length(variables_to_bc),
        " variable worker(s), ", worker_cores, " concurrent Rscript process(es)."
      )
      worker_results <- parallel::mclapply(
        variables_to_bc, run_variable_worker,
        mc.cores = worker_cores, mc.preschedule = FALSE
      )
